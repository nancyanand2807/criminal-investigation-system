% --- Executes on button press in pushbutton3.
function pushbutton3_Callback(hObject, eventdata, handles)
% hObject    handle to pushbutton3 (see GCBO)
% eventdata  reserved - to be defined in a future version of MATLAB
% handles    structure with handles and user data (see GUIDATA)
conn = database('cis','root','password','com.mysql.jdbc.Driver','jdbc:mysql://localhost:3306/cis');
selectquery = 'select max(id) from records';
curs = exec(conn,selectquery);
curs = fetch(curs,0);
myData = curs.Data;
id = myData{1,1}+1;
 %
 % write code to copy selected image into database
 %
 source=get(handles.edit1,'string');
 destination = getappdata(0,'db_loc');
 dest = strcat(destination,'\',num2str(id),'.jpg');
 copyfile(source, dest);
 
name=get(handles.edit4,'string');
sex=get(handles.edit5,'string');
dob=get(handles.edit6,'string');
add=get(handles.edit8,'string');
acc=get(handles.edit9,'string');

tablename = 'records';
colnames = {'id','name','sex','dob','address','accused_of'};
data = {id name sex dob add acc};
datainsert(conn,tablename,colnames,data);
close(gcf);
msgbox('inserted successfully');
front;
